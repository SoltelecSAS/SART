    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        jComboBox2.removeAllItems();
        
        int indiceSeleccionado = jComboBox1.getSelectedIndex();

        String condicionConsulta = indiceSeleccionado == 0 ? " IS NULL" : " = "+ indiceSeleccionado;

        String consultaSerial = Consultas.getSerialesByPrueba(condicionConsulta);

        try (Connection conexion = DriverManager.getConnection(url, usuario, contraseña);
             PreparedStatement consultaDelSerial = conexion.prepareStatement(consultaSerial)) {

            //rc representa el resultado de la consulta
            try (ResultSet rc = consultaDelSerial.executeQuery()) {
                while (rc.next()) {
                    //Colocara datos segun cada columna que encuentre
                    jComboBox2.addItem(rc.getString("NomEquipo")+": "+rc.getString("serialresolucion"));
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jComboBox1ActionPerformed

