    public FrmSeriales() throws SQLException {

        initComponents();

        ButtonGroup buttonGroup = new ButtonGroup();

        // Agregar los JRadioButtons al ButtonGroup
        buttonGroup.add(jRadioButton1);
        buttonGroup.add(jRadioButton2);

        if (jRadioButton1.isSelected()) {
            jComboBox1.setEnabled(true);  // Si jRadioButton1 está seleccionado, activar jComboBox1
            jComboBox2.setEnabled(false); // Desactivar jComboBox2
        } else if (jRadioButton2.isSelected()) {
            jComboBox1.setEnabled(false); // Desactivar jComboBox1
            jComboBox2.setEnabled(true);  // Si jRadioButton2 está seleccionado, activar jComboBox2
        }

        // Establecer uno de los JRadioButtons como seleccionado por defecto
        jRadioButton1.setSelected(true);
        
        Conexion.getInstance();

        String getTipoEquipo = Consultas.getTipoPruebas();

        url = Conexion.getUrl();
        usuario = Conexion.getUsuario();
        contraseña = Conexion.getContrasena();

        System.out.println(url);
        System.out.println(usuario);
        System.out.println(contraseña);
        
        try (Connection conexion = DriverManager.getConnection(url, usuario, contraseña);
             PreparedStatement consultaDagma = conexion.prepareStatement(getTipoEquipo)) {
            
            List<Enum1DTO> tipoPruebas = new ArrayList<>(); 

            //rc representa el resultado de la consulta
            try (ResultSet rc = consultaDagma.executeQuery()) {
                while (rc.next()) {
                    tipoPruebas.add(new Enum1DTO(
                        rc.getInt("TESTTYPE"), 
                        rc.getString("Nombre_tipo_prueba")));
                }
            }

            // Limpiar el modelo del JComboBox
            jComboBox1.removeAllItems();

            jComboBox1.addItem("Ninguna");

            // Ahora que tienes los elementos en tipoPruebas, puedes agregarlos al JComboBox
            for (Enum1DTO tipoPrueba : tipoPruebas) {
                jComboBox1.addItem(tipoPrueba.getDescripcion());
            }
        }   
    }

