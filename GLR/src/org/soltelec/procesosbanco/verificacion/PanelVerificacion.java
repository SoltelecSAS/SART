    /**
     * Repinta los rangos dependiendo si los valroes estan dentro del rango
     * de tolerancia para la norma
     */
    private void repintarDisplays() {
        
        displayMedidaHC.setLcdValue(medicion.getValorHC()/valorPef);
        displayMedidaCO.setLcdValue(medicion.getValorCO()*0.01);
        displayMedidaCO2.setLcdValue(medicion.getValorCO2()*0.1);
        displayMedidaO2.setLcdValue(medicion.getValorO2()*0.01);
        
        if(UtilVerificacion.estaEnRangoHC(tipoMuestra,(int)(medicion.getValorHC()/valorPef)) ){
            
            displayMedidaHC.setLcdColor(LcdColor.BLUEBLUE_LCD);
            
            
        } 
        else {
            
            displayMedidaHC.setLcdColor(LcdColor.RED_LCD);
            
        }
        
        if(UtilVerificacion.estaEnRangoCO(tipoMuestra,medicion.getValorCO()*0.01)){
            
            displayMedidaCO.setLcdColor(LcdColor.BLUEBLUE_LCD);
            
        } else {
            
            displayMedidaCO.setLcdColor(LcdColor.RED_LCD);
        }
        
        if(UtilVerificacion.estaEnRangoCO2(tipoMuestra,medicion.getValorCO2()*0.1)){
            
            displayMedidaCO2.setLcdColor(LcdColor.BLUEBLUE_LCD);
        }
        else {
            displayMedidaCO2.setLcdColor(LcdColor.RED_LCD);
            
        }
        this.repaint();
        //this.revalidate();
        
        
    }

